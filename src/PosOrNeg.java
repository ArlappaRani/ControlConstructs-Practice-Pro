
public class PosOrNeg {
	public int Number(int x)
	{
		if(x>0)//Checking condition  for checking the user input is positive or negative
		{
			System.out.println("Positive Number");//printing the if statement
		}
		else
		{
			System.out.println("Negative Number");//printing the else statement
		}
		return x;//returning value
	}

}
